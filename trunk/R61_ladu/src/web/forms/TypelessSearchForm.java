package web.forms;

public class TypelessSearchForm {

	String name;
	String description;
	String manufacturers_code;
	String manufacturer;
	String amount_in_warehouse;
	String sale_price_start;
	String sale_price_end;
	String warehouse_price_start;
	String warehouse_price_end;
	String attribute;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getManufacturers_code() {
		return manufacturers_code;
	}

	public void setManufacturers_code(String manufacturers_code) {
		this.manufacturers_code = manufacturers_code;
	}

	public String getManufacturer() {
		return manufacturer;
	}

	public void setManufacturer(String manufacturer) {
		this.manufacturer = manufacturer;
	}

	public String getAmount_in_warehouse() {
		return amount_in_warehouse;
	}

	public void setAmount_in_warehouse(String amount_in_warehouse) {
		this.amount_in_warehouse = amount_in_warehouse;
	}

	public String getSale_price_start() {
		return sale_price_start;
	}

	public void setSale_price_start(String sale_price_start) {
		this.sale_price_start = sale_price_start;
	}

	public String getSale_price_end() {
		return sale_price_end;
	}

	public void setSale_price_end(String sale_price_end) {
		this.sale_price_end = sale_price_end;
	}

	public String getWarehouse_price_start() {
		return warehouse_price_start;
	}

	public void setWarehouse_price_start(String warehouse_price_start) {
		this.warehouse_price_start = warehouse_price_start;
	}

	public String getWarehouse_price_end() {
		return warehouse_price_end;
	}

	public void setWarehouse_price_end(String warehouse_price_end) {
		this.warehouse_price_end = warehouse_price_end;
	}

	public String getAttribute() {
		return attribute;
	}

	public void setAttribute(String attribute) {
		this.attribute = attribute;
	}

}
