package sauts;

public class Coordinates {
	String la; //latitude
	String lo; //longitude
	/**
	 * @return the la
	 */
	public String getLa() {
		return la;
	}
	/**
	 * @param la the la to set
	 */
	public void setLa(String la) {
		this.la = la;
	}
	/**
	 * @return the lo
	 */
	public String getLo() {
		return lo;
	}
	/**
	 * @param lo the lo to set
	 */
	public void setLo(String lo) {
		this.lo = lo;
	}
	
}
